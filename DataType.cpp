#include <iostream>
using namespace std;

int main()
{

    // String
    // size n byte and double
    string name;
    name = "Vedant";
    cout << name << endl;

    // Character
    // size 1 byte and single
    char gender = 'M';
    cout << gender << endl;

    // Integer
    // size 4 byte
    int marks = 98;
    cout << marks << endl;

    // Float
    // size 4 byte
    float CPI = 8.52;
    cout << CPI << endl;

    // Double
    // size 8 byte
    double pi = 3.14159265358979323846;
    cout << pi << endl;

    // Boolean
    // size 1 byte
    bool male = true;
    cout << male << endl;

    return 0;
}